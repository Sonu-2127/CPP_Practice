#include <iostream>
#include <climits>
#include <cfloat>
using namespace std;

int main(void)
{
    cout << "maximum int value is "<< INT_MAX << endl;
    cout << "maximum float value is "<< FLT_MAX << endl;
    cout << "minimum int value is "<< INT_MIN << endl;
    cout << "minimum float value is "<< FLT_MIN << endl;
    cout << "minimum float value is "<< FLT_MIN << endl;
    cout << "minimum char value is "<< CHAR_MIN << endl;
    cout << "maximum char value is "<< CHAR_MAX << endl;
    cout << "maximum long value is "<< LONG_MAX << endl;
    cout << "maximum long long value is "<< LLONG_MAX << endl;
    cout << "minimum long value is "<< LONG_MIN << endl;
    cout << "minimum long long value is "<< LLONG_MIN << endl;
    return 0;
}